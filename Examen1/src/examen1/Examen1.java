    private void jb_registrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jb_registrarMouseClicked
        boolean usado = false;
        for (Usuario usuario : lista) {
            if (usuario.getUsuario().equals(ctf_usuario.getText())) {
                usado = true;
            }
        }
        if (ctf_usuario.getText().isEmpty() || ctf_password.getText().isEmpty() || ctf_correo.getText().isEmpty() || ctf_nombre.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor llene todas las casillas.");

        } else if (usado) {
            JOptionPane.showMessageDialog(this, "Usuario ya usado.");
        } else {
            try {
                lista.add(new Usuario());
                lista.get(lista.size() - 1).setUsuario(ctf_usuario.getText());
                lista.get(lista.size() - 1).setPassword(ctf_password.getText());
                lista.get(lista.size() - 1).setNacimiento(cjc_fecha.getDate());
                lista.get(lista.size() - 1).setTelefono(Integer.parseInt(ctf_numero.getText()));
                lista.get(lista.size() - 1).setCorreo(ctf_correo.getText());
                lista.get(lista.size() - 1).setNombre(ctf_nombre.getText());
                lista.get(lista.size() - 1).setGenero(ccb_genero.getSelectedItem().toString());
                creador.dispose();
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, e);
            }
        }
    }//GEN-LAST:event_jb_registrarMouseClicked

